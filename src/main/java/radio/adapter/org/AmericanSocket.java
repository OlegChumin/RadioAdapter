package radio.adapter.org;

/**
 * Интерфейс {@code AmericanSocket} моделирует американскую розетку.
 * <p>
 * Используется в паттерне "Адаптер" для имитации получения питания
 * от американского стандарта розетки.
 * </p>
 *
 * <p>Особенности:
 * <ul>
 *   <li>Метод {@link #getPower()} символизирует подачу питания.</li>
 *   <li>Конкретные реализации могут эмулировать разное напряжение или частоту.</li>
 * </ul>
 * </p>
 */
interface AmericanSocket {

    /**
     * Получить питание от американской розетки.
     * <p>
     * Метод должен вызываться клиентским кодом для моделирования
     * подключения к источнику питания.
     * </p>
     */
    void getPower();
}
