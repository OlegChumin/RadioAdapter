package radio.adapter.org;

/**
 * Интерфейс {@code EuroSocket} моделирует европейскую розетку.
 * <p>
 * Используется в паттерне "Адаптер" для имитации получения питания
 * от европейского стандарта розетки (обычно 220В, 50Гц).
 * </p>
 *
 * <p>Особенности:
 * <ul>
 *   <li>Метод {@link #getPower()} символизирует подачу питания.</li>
 *   <li>Конкретные реализации могут эмулировать особенности европейского стандарта.</li>
 * </ul>
 * </p>
 */
interface EuroSocket {

    /**
     * Получить питание от европейской розетки.
     * <p>
     * Метод должен вызываться клиентским кодом для моделирования
     * подключения к источнику питания.
     * </p>
     */
    void getPower();
}
